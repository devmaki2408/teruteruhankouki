def build_five_forces_prompt(idea_title: str, idea_overview: str) -> str:
    return f"""
あなたは戦略コンサルタントです。
以下の事業案について、Porterの5Forces分析を行ってください。

【事業案タイトル】
{idea_title}

【事業案概要】
{idea_overview}

【目的】
新規事業担当者が、この事業案の競争環境を短時間で把握できるようにすることです。

【ルール】
- 日本語で出力してください
- 高校生にもわかる表現で書いてください
- 各項目について、score は 1〜5 の整数で評価してください
- score が高いほど圧力・脅威・競争の強さが高いことを意味します
- 各理由は1〜2文で簡潔に書いてください
- 必ずJSONのみを出力してください

以下のJSONのみを返してください。
{{
  "five_forces": {{
    "industry_rivalry": {{
      "score": 1,
      "reason": "理由"
    }},
    "threat_of_new_entry": {{
      "score": 1,
      "reason": "理由"
    }},
    "threat_of_substitutes": {{
      "score": 1,
      "reason": "理由"
    }},
    "buyer_power": {{
      "score": 1,
      "reason": "理由"
    }},
    "supplier_power": {{
      "score": 1,
      "reason": "理由"
    }}
  }}
}}

条件:
- score は 1〜5 の整数
- 余計な文字は出さない
""".strip()


def build_member_reason_prompt(
    idea_title: str,
    member_name: str,
    member_skills_json: str,
) -> str:
    return f"""
あなたは新規事業の組成に強い人材アサインの専門家です。
以下の事業案に対して、この人材が適任な理由を説明してください。

【事業案タイトル】
{idea_title}

【メンバー名】
{member_name}

【メンバーのスキル】
{member_skills_json}

【目的】
新規事業担当者が、「なぜこの人がこの事業に適しているのか」を理解できるようにすることです。

【ルール】
- 日本語で出力してください
- 高校生にもわかる表現で書いてください
- 理由は1〜2文で簡潔に書いてください
- スキルと事業案の関係が伝わるようにしてください
- 必ずJSONのみを出力してください

以下のJSONのみを返してください。
{{
  "member_reason": {{
    "member_name": "{member_name}",
    "reason": "推奨理由"
  }}
}}

条件:
- 余計な文字は出さない
""".strip()