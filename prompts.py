def build_idea_generation_prompt(target: str, issue_title: str, issue_description: str) -> str:
    return f"""
あなたは優秀な新規事業プランナーです。
選択されたターゲットと課題に対して、実現可能性と新規性のある事業案を5件生成してください。

【ターゲット】
{target}

【課題タイトル】
{issue_title}

【課題説明】
{issue_description}

【目的】
新規事業担当者が、課題に対する具体的な事業案を比較・選択できるようにすることです。

【ルール】
- 日本語で出力してください
- 高校生にもわかる表現で書いてください
- 5件とも異なる方向性の事業案にしてください
- IT / AI / IoT / マッチング / サブスク / プラットフォームなどの技術・手法を適切に活用してください
- 机上の空論ではなく、最低限「事業として成立しそう」なものにしてください
- 既存サービスの単なる言い換えにはしないでください
- スコアは100点満点で評価してください
- スコア理由も簡潔に示してください
- 必ずJSONのみを出力してください

以下のJSONのみを返してください。
{{
  "ideas": [
    {{
      "title": "事業案タイトル",
      "overview": "事業案の概要",
      "solution": "解決方法",
      "value": "提供価値",
      "score": 80,
      "reason": "点数理由"
    }}
  ]
}}

条件:
- 事業案を5件返す
- 配列キーは ideas
- score は 0〜100 の整数
- 余計な文字は出さない

【スコアの観点】
- 顧客課題への適合度
- 市場性
- 実現可能性
- 収益化可能性
- 新規性
""".strip()


